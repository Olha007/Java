package lesson6;

/**
 * 1. Створіть клас MyPyramid із загальнодоступним статичним методом printPyramid(int h), який приймає як параметр ціле число h від 1 до 9 і друкує піраміду чисел висотою h.
 * Приклад: для h = 4 метод повинен надрукувати піраміду
 * 1
 * 121
 * 12321
 * 1234321
 * 2. Напишіть клас Main з методом main, у якому який перевірте роботу методу printPyramid(int h).
 * 3. Зазначте у документованому коментарі класів їх призначеня, а також своє ім'я та прізвище.
 *
 * @version 1.1
 * @autor Olha Nozdriukhina
 */

public class MyPyramid {

    /**
     * Друкує піраміду чисел зі зростаючими та зменшуючими послідовностями.
     *
     * @param h Висота піраміди (ціле число від 1 до 9)
     */
    public static void printPyramid(int h) {
        if (h < 1 || h > 9) {
            System.out.println("Висота піраміди повинна бути від 1 до 9.");
            return;
        }

        for (int i = 1; i <= h; i++) {
            for (int j = h - i; j > 0; j--) {
                System.out.print(" ");
            }

            // Друкуємо зростаючу послідовність чисел
            for (int j = 1; j <= i; j++) {
                System.out.print(j);
            }

            // Друкуємо зменшуючу послідовність чисел (без останнього числа, яке дублюється)
            for (int j = i - 1; j >= 1; j--) {
                System.out.print(j);
            }

            System.out.println();
        }
    }
}


